#include <unistd.h>
#include <stdio.h>

int	main(int argc, char **argv)
{
	int i;
	int j;

	j = 0;
	i = 0;
	while (++j < argc)
	{
		while (argv[j][i] != '\0')
		{
			write(1, &argv[j][i], 1);
			i++;
		}
		write(1, "\n", 1);
		i = 0;
	}
	return (0);
}
